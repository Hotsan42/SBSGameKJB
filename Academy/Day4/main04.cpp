#include <iostream>

using namespace std;

void main() 
{
	//반복문
	//배열과 반복문은 뗄레야 뗄 수 없고, 반복문이 본체!

	//반복문-반복해서 동작할때, 유용하게 해주는 문법

	for (int i = 0; i < 10; i++)
	{
		cout << "i : " << i << endl;
	}

	//for (선언식; 조건식; 증감문) { 실행 구문 }

	//선언식 - 반복문을 사용할 때 (선언식)이라는 변수를 선언해서 사용, for문 시작시 딱 한 번 실행

	//조건식 - 조건식이 true면 실행구문을 실행하겠다.

	//증감식 - 실행구문을 완료하고, 실행시킬 간단한 증감식을 넣을수 있음. 안적어도 무방.

	//
}